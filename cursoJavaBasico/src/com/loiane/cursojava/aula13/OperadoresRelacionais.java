package com.loiane.cursojava.aula13;

public class OperadoresRelacionais {

	public static void main(String[] args) {
		
		int valor1 = 1;
		int valor2 = 2;
		
		System.out.println("Valor1 é IGUAL (==) ao Valor2: " + (valor1 == valor2));
		System.out.println("Valor1 é DIFERENTE (!=) do Valor2: " + (valor1 != valor2));
		System.out.println("Valor1 MAIOR QUE (>) o Valor2: " + (valor1 > valor2));
		System.out.println("Valor1 MAIOR OU IGUAL QUE (>=) o Valor2: " + (valor1 >= valor2));
		System.out.println("Valor1 MENOR QUE (<) o Valor2: " + (valor1 < valor2));
		System.out.println("Valor1 MENOR OU IGUAL QUE (<=) o  Valor2: " + (valor1 <= valor2));
		



	}

}
