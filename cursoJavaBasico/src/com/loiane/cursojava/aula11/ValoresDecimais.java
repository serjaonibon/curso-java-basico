package com.loiane.cursojava.aula11;

public class ValoresDecimais {

	public static void main(String[] args) {
		int decVal = 26;
		int hexVal = 0x1a;
		int octVal = 032;
		int binVal = 0b11010;
		
		System.out.println(decVal);
		System.out.println(hexVal);
		System.out.println(octVal);
		System.out.println(binVal);

	}

}
